#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <dirent.h>

#define MAX_FILES 256
#define MAX_PATH 512
#define MAX_SLUG 128
#define MAX_TITLE 256
#define MAX_DATE 32

struct post_meta {
	char path[MAX_PATH];
	char slug[MAX_SLUG];
	char title[MAX_TITLE];
	char date[MAX_DATE];
};

static void parse_frontmatter(const char *path, char *title, char *date)
{
	FILE *f = fopen(path, "r");
	char line[1024];
	int in_frontmatter = 0;
	int separator_count = 0;

	if (!f) return;

	/* Defaults */
	strcpy(title, "Untitled");
	strcpy(date, "1970-01-01");

	while (fgets(line, sizeof(line), f)) {
		size_t len = strlen(line);
		if (len > 0 && line[len-1] == '\n') line[len-1] = '\0';
		if (len > 0 && line[len-1] == '\r') line[len-1] = '\0';

		if (strncmp(line, "---", 3) == 0) {
			separator_count++;
			if (separator_count == 1) in_frontmatter = 1;
			if (separator_count == 2) break; /* End of frontmatter */
			continue;
		}

		if (in_frontmatter) {
			if (strncmp(line, "title:", 6) == 0) {
				char *p = line + 6;
				while (*p == ' ' || *p == '"') p++;
				strncpy(title, p, MAX_TITLE - 1);
				title[MAX_TITLE - 1] = '\0';
				/* Remove trailing quote if exists */
				if (title[strlen(title)-1] == '"')
					title[strlen(title)-1] = '\0';
			}
			else if (strncmp(line, "date:", 5) == 0) {
				char *p = line + 5;
				while (*p == ' ' || *p == '"') p++;
				strncpy(date, p, MAX_DATE - 1);
				date[MAX_DATE - 1] = '\0';
				if (date[strlen(date)-1] == '"')
					date[strlen(date)-1] = '\0';
			}
		}
	}
	fclose(f);
}

static int compare_posts(const void *a, const void *b)
{
	const struct post_meta *pa = (const struct post_meta *)a;
	const struct post_meta *pb = (const struct post_meta *)b;
	/* Descending order (newest first) */
	return strcmp(pb->date, pa->date);
}

static void print_hex(const char *path, const char *slug)
{
	FILE *f = fopen(path, "rb");
	if (!f) return;

	printf("static const char post_data_%s[] = {\n\t", slug);
	int c, count = 0;
	while ((c = fgetc(f)) != EOF) {
		printf("0x%02x, ", c);
		if (++count % 12 == 0)
			printf("\n\t");
	}
	/* Null terminator */
	printf("0x00\n};\n\n");
	fclose(f);
}

int main(int argc, char **argv)
{
	DIR *d;
	struct dirent *dir;
	struct post_meta files[MAX_FILES];
	int i, count = 0;

	if (argc < 2)
		return 1;

	d = opendir(argv[1]);
	if (!d)
		return 1;

	printf("/* Auto-generated by packer.c - DO NOT EDIT */\n\n");

	/* First pass: Collect metadata and sort (if needed, currently raw order) */
	while ((dir = readdir(d)) != NULL) {
		if (strstr(dir->d_name, ".md")) {
			char *ext;
			
			if (count >= MAX_FILES) break;

			/* Path */
			snprintf(files[count].path, MAX_PATH, "%s/%s", argv[1], dir->d_name);

			/* Slug */
			strncpy(files[count].slug, dir->d_name, MAX_SLUG - 1);
			ext = strstr(files[count].slug, ".md");
			if (ext) *ext = '\0';

			/* Sanitize slug for C variable name */
			for (i = 0; files[count].slug[i]; i++)
				if (files[count].slug[i] == '-') files[count].slug[i] = '_';

			/* Parse Title/Date */
			parse_frontmatter(files[count].path, files[count].title, files[count].date);

			count++;
		}
	}
	closedir(d);

	/* Sort posts by date descending */
	qsort(files, count, sizeof(struct post_meta), compare_posts);

	/* Dump content arrays */
	for (i = 0; i < count; i++) {
		print_hex(files[count].path, files[count].slug); // Re-reading path to dump hex, slightly inefficient but simple
		/* Optimization: print_hex uses the path, not the stored struct path which might be same */
		/* Actually, let's use the stored path to be safe */
		print_hex(files[i].path, files[i].slug);
	}

	/* Generate 'posts' array for Index */
	printf("/* Blog Index (Metadata) */\n");
	printf("const struct blog_post posts[] = {\n");
	for (i = 0; i < count; i++) {
		/* Reverse order? Usually blogs are newest first. 
		   Parsing directory order is undefined.
		   For now, we output as is. Ideally we sort by date. */
		printf("\t{\n");
		printf("\t\t.title = \"%s\",\n", files[i].title);
		printf("\t\t.date  = \"%s\",\n", files[i].date);
		printf("\t\t.slug  = \"%s\"\n", files[i].slug); // Using sanitized slug as ID
		printf("\t}%s\n", (i < count - 1) ? "," : "");
	}
	printf("};\n\n");
	printf("const int posts_count = %d;\n\n", count);

	/* Generate Lookup Table for content */
	printf("struct embedded_post {\n\tconst char *slug;\n\tconst char *data;\n};\n\n");
	printf("static const struct embedded_post embedded_posts[] = {\n");
	for (i = 0; i < count; i++) {
		printf("\t{ \"%s\", post_data_%s },\n", files[i].slug, files[i].slug);
	}
	printf("\t{ NULL, NULL }\n};\n\n");

	/* Getter function */
	printf("const char *get_article_body(const char *slug)\n{\n");
	printf("\tint i;\n\tfor (i = 0; embedded_posts[i].slug; i++) {\n");
	printf("\t\tif (strcmp(embedded_posts[i].slug, slug) == 0)\n");
	printf("\t\t\treturn embedded_posts[i].data;\n\t}\n");
	printf("\treturn NULL;\n}\n");

	return 0;
}
